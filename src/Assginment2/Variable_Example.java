package Assginment2;
public class Variable_Example {
    public static void main(String[] args) {
        // Variable declaration and initialization
        int age = 26; // Integer variable
        double height = 5.10; // Double variable
        char grade = 'A'; // Character variable
        String name = "Soykat"; // String variable
        boolean is_student = true; // Boolean variable

        // Printing the variables
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + is_student);
    }
}

